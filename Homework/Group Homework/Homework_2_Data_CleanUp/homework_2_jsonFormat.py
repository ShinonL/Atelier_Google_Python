json_dataset = {}
years = [year for year in range(2011, 2020)]
for country, coverage_list in dataset:
    coverages = []
    for coverage in coverage_list:
        try:
            coverages.append(int(''.join(filter(str.isdigit, coverage))))
        except Exception as e:
            coverages.append(0)
    json_dataset[country] = [{'year': year, 'coverage': coverage}
                             for year, coverage in zip(years, coverages)]


def get_year_data(dataset, string_year):
    try:
        searched_year = int(string_year)
        assert (searched_year in years), 'Data for the year ' + \
            string_year + ' does not exist.'
    except ValueError:
        return 'Given year data is not a valid integer.'
    except AssertionError as ae:
        return ae

    result = {string_year: []}
    for country in dataset:
        for data in dataset[country]:
            if data['year'] == searched_year:
                result[string_year].append((country, data['coverage']))

    return result


def get_country_data(dataset, searched_country):
    for country in dataset:
        if country == searched_country:
            return {country: [(data['year'], data['coverage']) for data in dataset[country]]}
    return 'Country not found.'


def perform_average(dataset, searched_country):
    for country in dataset:
        if country == searched_country:
            coverage = [data['coverage'] for data in dataset[country]]
            return round(mean(coverage), 1)
    return 'Country not found.'


